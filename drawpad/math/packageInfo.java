/**
 * This package provides classes and utilities for working with 2D vectors in a
 * graphics engine. It includes a Vector2D class for representing 2D vectors,
 * as well as classes for performing common vector operations and mathematical
 * functions.
 *
 * <p>The Vector2D class represents a 2D vector with x and y components. It is
 * immutable and provides methods for common vector operations like magnitude,
 * angle, and normalization. The class also overloads the +, -, *, and /
 * operators for vector addition, subtraction, scalar multiplication, and scalar
 * division, respectively.
 *
 * <p>The Vector2DOperations class contains static methods for performing vector
 * operations like addition, subtraction, scalar multiplication, dot product,
 * and cross product (for 2D, cross product returns a scalar).
 *
 * <p>The VectorMath class provides utility methods for vector mathematics,
 * including rotation, projection, linear interpolation, and magnitude clamping.
 *
 * <p>All classes in this package are immutable and thread-safe.
 */
